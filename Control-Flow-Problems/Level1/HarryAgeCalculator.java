// Class to compute Harry's age in 2024
class ComputeAge {
    public static void main(String[] args) {

        // Create a variable for name
        String name = "Harry";

        // Create variables for birth year and current year
        int birthYear = 2000;
        int currentYear = 2024;

        // Compute age
        int age = currentYear - birthYear;

        // Display result
        System.out.println(name + "'s age in " + currentYear + " is " + age);
    }
}
