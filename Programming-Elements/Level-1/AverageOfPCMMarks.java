public class AverageOfPCMMarks {
    public static void main(String[] args) {
        // Sam's marks in PCM
        int maths = 94;
        int physics = 95;
        int chemistry = 96;
        
        // Calculate average
        double average = (maths + physics + chemistry) / 3.0;
        
        // Display the result
        System.out.println("Sam's average mark in PCM is " + average);
    }
}
